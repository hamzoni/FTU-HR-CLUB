<p><b>Mục tiêu</b></p>
<p>Quản trị các nguồn Tài chính trong Doanh nghiệp bao gồm vốn, chi phí, doanh thu kinh doanh và lợi nhuận kinh doanh giúp Doanh nghiệp đạt được kiểm soát tài chính và tăng trưởng bền vững.</p>

<p><b>Mô tả công việc</b></p>
<p>Chiến lược: Nghiên cứu, phân tích, xây dựng các kế hoạch Tài chính ngắn hạn, trung hạn và dài hạn cho Doanh nghiệp phù hợp với mục tiêu Kinh doanh.</p>
<p>Điều hành: Tổ chức và chỉ đạo hoạt động Tài chính nhằm tối đa hóa hiệu quả sử dụng Tài chính, bao gồm:</p>
<p>Lựa chọn nguồn vốn hoạt động cho Doanh nghiệp</p>
<p>Quản lý ngân sách Doanh nghiệp</p>
<p>Giảm gánh nặng thuế</p>
<p>Tăng cường hiệu quả sử dụng vốn qua việc quản lý các quỹ Tài chính.</p>
<p>Kiểm soát: Thực hiện các nghiệp vụ Kế toán, kiểm soát, quản lý rủi ro, bảo toàn tài sản,... đảm bảo Doanh nghiệp tuân thủ các chuẩn mực Tài chính và nhằm quản trị rủi ro Tài chính.</p>
<p>Phối hợp đưa ra quyết định: Cùng với các phòng ban khác trong Doanh nghiệp như bộ phận Sales, Marketing, Logistics, Human Resources,... để đưa ra các quyết định quan trọng liên quan đến Chiến lược hoạt động của Doanh nghiệp</p>

<p><b>Yêu cầu công việc</b></p>
<p>Học vấn</p>
<p>Được đào tạo trong các Chuyên ngành như Kế toán, Kiểm toán, Tài chính, Quản trị, Ngân hàng;.........</p>
<p>Có kiến thức sâu rộng về Tài chính, Đầu tư, Quản trị rủi ro, Quản trị Kinh doanh</p>

