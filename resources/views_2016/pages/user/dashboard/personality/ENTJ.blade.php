<p>Chào bạn, nhóm tính cách của bạn là <b>ENTJ</b>.</p>

<p>Một số đặc trưng của nhóm tính cách này:</p>

<ul>
    <li><b>ENTJ</b> luôn muốn biến lý thuyết thành thực tiễn.</li>
    <li><b>ENTJ</b> muốn mọi thứ phải luôn có tổ chức, ngăn nắp và kỷ luật.</li>
    <li><b>ENTJ</b> có sức mạnh cá nhân lớn có thể giúp họ hoàn thành tốt những mục tiêu đề ra của bản thân</li>
    <li><b>ENTJ</b> là nhà lãnh đạo bẩm sinh vì họ rất nhanh nhẹn khi nắm bắt những vấn đề phức tạp và quyết đoán khi đưa ra quyết định</li>
</ul>

<p>Với những đặc trưng chính như vậy, <b>ENTJ</b> phù hợp với các ngành nghề:</p>

<h3>1. Quản lý kế hoạch tài chính - Finance</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Lập kế hoạch tài chính - Xác định nhu cầu vốn cho doanh nghiệp</li>
            <li>Đánh giá môi trường kinh doanh</li>
            <li>Xác định tầm nhìn kinh doanh và mục tiêu</li>
            <li>Xác định các nguồn lực cần để hoàn thành mục tiêu kinh doanh</li>
            <li>Định lượng tổng chi phí, lập bảng ngân sách</li>
            <li>Xác định rủi ro có thể gặp phải với bảng ngân sách</li>
            <li>Xác định cơ cấu vốn cho doanh nghiệp</li>
            <li>Đưa ra lựa chọn nguồn vốn tối ưu</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu ứng viên</b>

        <ul>
            <li>Tư duy logic, kỹ năng tổng hợp, phân tích và phán đoán dựa trên xâu chuỗi các sự kiện</li>
            <li>Tư duy hệ thống, nhìn nhận vấn đề một cách toàn diện</li>
            <li>Kỹ năng tính toán, định lượng</li>
            <li>Kỹ năng chú ý đến chi tiết, tỉ mỉ và cẩn trọng</li>
            <li>Kỹ năng phân tích dữ liệu, nắm bắt nhanh các con số</li>
            <li>Kỹ năng sắp xếp thứ tự ưu tiên để đưa ra quyết định</li>
            <li>Kỹ năng phân tích, quản lý rủi ro</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>

<ul>
    <li>Một nhà quản lý kế hoạch tài chính cần có tính cách E - Extrovert để có khả năng giao tiếp xuất sắc, để diễn đạt ý kiến và quyết định của mình với người khác.</li>
    <li>Trung tâm N - “Trực giác” của não bộ chịu trách nhiệm tìm hiểu, diễn dịch, và hình thành các mô hình từ thông tin thu thập được; sắp xếp các mô hình và liên hệ chúng với nhau. Nó giúp cho não bộ suy đoán các khả năng và tiên đoán tương lai.</li>
    <li>Phần lý trí T - Thinking trong não bộ con người phân tích thông tin một cách khách quan, làm việc dựa trên đúng/sai, suy luận và đưa ra kết luận một cách có hệ thống. Nó là bản chất logic của con người. Những kỹ năng phân tích, tính toán, định lượng của một người quản lý kế hoạch tài chính rất cần đến T - Thinking.</li>
    <li>Những người có tính cách J - Judging (Nguyên tắc) thường tiếp cận cận thế giới một cách có kế hoạch, có tổ chức. Họ có khả năng nhìn xa trông rộng, và thường rất thành công trong việc đưa ra những kế hoạch để thay đổi tình thế – đặc biệt là những vấn đề mang tính đoàn thể.</li>
</ul>

<h3>2. Kiểm soát tài chính - Finance</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Phân tích tình hình tài chính qua các báo cáo thu nhập, báo cáo sử dụng vốn, hệ thống tài chính - kế toán</li>
            <li>So sánh số liệu kết quả thực tế với kế hoạch đã đặt ra để đánh giá hiệu quả Tài chính, đưa ra những tư vấn chiến lược cho Ban giám đốc.</li>
            Yêu cầu ứng viên
            <li>Kỹ năng tính toán, định lượng</li>
            <li>Tư duy phản biện, đánh giá và nhìn nhận mọi vấn đề theo nhiều cách khác nhau</li>
            <li>Kỹ năng rà soát lỗi</li>
            <li>Kỹ năng chịu đựng áp lực cao</li>
            <li>Kỹ năng phân tích dữ liệu</li>
            <li>Kỹ năng thuyết trình và thuyết phục</li>
        </ul>
    </li>
</ul>

<b>Lý do phù hợp</b>

<ul>
    <li>ENTJ thích tương tác với con người. Là những người hướng ngoại (E - Extrovert), họ rất năng động và bị kích thích chủ yếu bởi những tác nhân bên ngoài. Không có gì làm cho ENTJ thích thú và thỏa mãn hơn một cuộc tranh luận sôi nổi và đầy thử thách, một tính cách cần thiết cho người kiểm soát tài chính trong công ty.</li>
    <li>Chuyên viên kiểm soát tài chính cần có T - Thinking (Lý trí) trong việc dùng lập luận logic để thấu hiểu vấn đề, và đưa ra những quyết định quan trọng.</li>
    <li>Những người kiểm soát tài chính là những người rất nguyên tắc (J - Judging), họ không thích nhìn thấy những sai sót bị lặp lại, họ chỉn chu, tỉ mỉ và cẩn thận trong công việc kiểm soát tình hình tài chính, đưa ra chiến lược của mình.</li>
</ul>

<p>Tuy nhiên, trên đây chỉ là danh sách nghề nghiệp có thể sẽ thích hợp cho một <b>ENTJ</b> như bạn. Mục đích của nó là cho bạn một sự tham khảo chứ không phải là một bản danh sách chuẩn. Không có bất cứ một cam kết nào chứng tỏ rằng những sự nghiệp trên sẽ phù hợp hoàn toàn với bạn, nhưng cũng có thể, bạn sẽ tìm thấy nghề nghiệp phù hợp nhất cho bản thân trong danh sách này.</p>

