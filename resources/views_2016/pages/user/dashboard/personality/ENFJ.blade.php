<p>Chào bạn, nhóm tính cách của bạn là <b>ENFJ.</b></p>

<p>Một số đặc trưng của nhóm tính cách này:</p>

<ul>
    <li><b>ENFJ</b> rất chân thành và nhiệt tình quan tâm đến mọi người.</li>
    <li><b>ENFJ</b> đánh giá cao cấu trúc và tổ chức và thích tự mình tạo nên những cấu trúc đó.</li>
    <li><b>ENFJ</b> cực kì nhạy cảm với những lời phê bình và xích mích.</li>
    <li><b>ENFJ</b> cần sự tán thành và ủng hộ của người khác để cảm thấy hài lòng với bản thân.</li>
</ul>

<p>Với những đặc trưng chính như vậy, <b>ENFJ</b> phù hợp với các ngành nghề:</p>

<h3>1. Quản lý nhân sự - Human Resources</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>
                Xây dựng và ban hành các quy chế khen thưởng, báo cáo, xử phạt.

                <ul>
                    <li>
                        Đánh giá nhân viên: Luôn theo sát kết quả làm việc của nhân viên để có được đánh giá khách quan, chính xác nhất. Khi một nhân viên làm việc tốt, họ xứng đáng được khen thưởng, tuyên dương.
                    </li>

                    <li>
                        Trả công nhân viên: Trả lương đầy đủ, đúng thời gian để nhân sự yên tâm làm việc và cống hiến.
                    </li>
                </ul>
            </li>

            <li>
                Mối quan hệ giữa các nhân viên: Tăng cường các hoạt động ngoài giờ nhằm thúc đẩy tinh thần gắn kết giữa các nhân viên, đó là một trong những yếu tố quan trọng để phát triển doanh nghiệp.
            </li>

            <li>
                Truyền đạt thông tin hiệu quả nhanh chóng, phân phối lợi ích cho người lao động và giải quyết các vấn đề tranh chấp của người lao động.
            </li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu ứng viên</b>

        <ul>
            <li>Chân thành và nhiệt tình quan tâm đến mọi người.</li>
            <li>Có hiểu biết nhất định về tâm lý để có thể thấu hiểu và thông cảm với các hành vi của người lao động.</li>
            <li>Kỹ năng giải quyết tình huống và giải quyết mâu thuẫn một cách hiệu quả.</li>
            <li>Yêu cầu Ứng viên cao về trí thông minh cảm xúc EQ. Đặc biệt giỏi trong việc đối nhân xử thế.</li>
            <li>Chăm chỉ, cẩn trọng, chi tiết.</li>
            <li>Sáng suốt và bình tĩnh trong việc ra quyết định. Các quyết định thực hiện dựa trên các dữ kiện thu thập được.</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>

<ul>
    <li>Một người quản lý nhân sự đòi hỏi sự khéo léo, thông minh trong giao tiếp với mọi người, đặc biệt tính cách hướng ngoại cần phát huy trong việc đối nhân xử thế, ví dụ trong tình huống mâu thuẫn nội bộ. Chính vì thế, đặc tính E - Extrovert rất phù hợp với một người như vậy.</li>
    <li>Trung tâm N - “Trực giác” của não bộ chịu trách nhiệm tìm hiểu, diễn dịch, và hình thành các mô hình từ thông tin thu thập được; sắp xếp các mô hình và liên hệ chúng với nhau. Nó giúp cho não bộ suy đoán các khả năng và tiên đoán tương lai. Và một trong những yêu cầu cần có của người quản lý nhân sự là khả năng đưa ra các quyết định thực hiện dựa trên các dữ kiện thu thập được.</li>
    <li>Quản lý nhân sự cần có trí thông minh cảm xúc EQ cao để có thể thấu hiểu và thông cảm với các hành vi của người lao động trong các mối quan hệ xã hội và quan hệ với môi trường xung quanh nên tính cách F - Feeling là rất cần thiết.</li>
    <li>Quản lý và duy trì nhân sự yêu cầu Ứng viên phải nhạy cảm và không chấp nhận những sai sót từ người khác. Một ENTJ có tính cách J - Judging phát triển rất phù hợp với tính chất công việc này: họ minh bạch, thông thái, trách nhiệm và có phần nghiêm khắc.</li>
</ul>

<h3>2. Quản lý quan hệ khách hàng - Logistics</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Lên kế hoạch và chỉ đạo hoạt động của đội ngũ chăm sóc khách hàng để đáp ứng mọi nhu cầu khách hàng.</li>
            <li>Phát triển quy trình, thiết lập các tiêu chuẩn, giám sát hoạt động quản lý đảm bảo đơn hàng chính xác, hiệu quả và nhanh chóng đến tay khách hàng.</li>
            <li>Chịu trách nhiệm đáp ứng các yêu cầu và giải quyết các vấn đề của khách hàng</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu ứng viên</b>

        <ul>
            <li>Khả năng giao tiếp khéo léo để làm việc với khách hàng</li>
            <li>Sự nhanh nhạy, linh hoạt trong công việc</li>
            <li>Năng động, sáng tạo, nhiệt huyết, có tinh thần trách nhiệm cao</li>
            <li>Khả năng bao quát, giải quyết tình huống</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>

<ul>
    <li>Một quản lý quan hệ khách hàng trong Logistics rất cần có khả năng giao tiếp khéo léo để làm việc với khách hàng. Chính vì thế, đặc tính E - Extrovert rất phù hợp với một người như vậy.</li>
    <li>Quản lý quan hệ khách hàng là một nghề đòi hỏi sự nhanh nhẹn, linh hoạt trong công việc, nên họ cần có N - “Trực giác” nhạy bén.</li>
    <li>Việc làm việc với con người không thể hoàn toàn dựa vào lý trí hay những quy tắc khô khan, những người quản lý quan hệ khách hàng cần có trí thông minh cảm xúc EQ cao để có khả năng linh hoạt trong xử lý tình huống, nên họ cần có F - Feeling</li>
    <li>Nghề quản lý quan hệ khách hàng yêu cầu Ứng viên phải nhạy cảm và không chấp nhận những sai sót từ người khác. Một ENTJ có tính cách J - Judging phát triển rất phù hợp với tính chất công việc này: họ minh bạch, thông thái và rất trách nhiệm.</li>
</ul>

<p>Tuy nhiên, trên đây chỉ là danh sách nghề nghiệp có thể sẽ thích hợp cho một <b>ENFJ</b> như bạn. Mục đích của nó là cho bạn một sự tham khảo chứ không phải là một bản danh sách chuẩn. Không có bất cứ một cam kết nào chứng tỏ rằng những sự nghiệp trên sẽ phù hợp hoàn toàn với bạn, nhưng cũng có thể, bạn sẽ tìm thấy nghề nghiệp phù hợp nhất cho bản thân trong danh sách này.</p>
