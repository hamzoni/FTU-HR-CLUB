<p>Chào bạn, tính cách của bạn là <b>INFJ</b>.</p>

<p>Một số đặc trưng của người mang tính cách <b>INFJ</b>:</p>

<ul>
    <li><b>INFJ</b> hướng đến việc giúp đỡ những người đang cần - họ có thể vội vàng đến nơi một thảm họa lớn, tham gia vào nỗ lực cứu hộ, làm công việc từ thiện, ...</li>
    <li><b>INFJ</b> là sự kết hợp độc đáo của chủ nghĩa lý tưởng và quyết đoán - điều này có nghĩa rằng sự sáng tạo và trí tưởng tượng của họ có thể được dẫn thẳng tới một mục tiêu cụ thể.</li>
    <li>Các <b>INFJ</b> là bậc thầy về giao tiếp bằng văn bản, với một ngôn ngữ đặc sắc mượt mà và ấm áp.</li>
    <li>Sự nhạy cảm của <b>INFJ</b> cho phép họ kết nối với những người khác khá dễ dàng. Giao tiếp dễ dàng và sự dễ chịu của họ thường có thể gây hiểu lầm cho những người xung quanh, những người đó có thể nghĩ rằng <b>INFJ</b> thực sự là hướng ngoại.</li>
    <li>Các <b>INFJ</b> hết sức quan tâm đến cảm xúc của người khác và mong đợi họ trả ơn.</li>
</ul>

<p>Với những đặc trưng chính như vậy, INFP thường có xu hướng phù hợp với các ngành nghề:</p>

<h3>1. Tuyển dụng nhân sự - Human Resources</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Thiết lập chiến dịch tuyển dụng nhân sự</li>
            <li>Phân tích nhu cầu sử dụng lao động của doanh nghiệp</li>
            <li>Xác định những vấn đề tuyển dụng cụ thể: doanh nghiệp cần có những lao động như thế nào? Thời điểm nào nên tuyển dụng? Những lao động hiện tại của doanh nghiệp đã đáp ứng được yêu cầu công việc chưa?</li>
            <li>Giám sát, tư vấn quá trình tuyển dụng.</li>
            <li>Hoạch định chính sách về chế độ của công ty để thu hút nhân lực.</li>
            <li>Điều phối phòng nhân sự tham gia tuyển dụng các vị trí còn thiếu cho các phòng ban.</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu ứng viên</b>

        <ul>
            <li>Tư duy nhìn nhận vấn đề hệ thống, logic.</li>
            <li>Có tầm nhìn chiến lược về định hướng phát triển của công ty. Có khả năng nhạy bén về các thay đổi trong cơ cấu công ty để có các kế hoạch hoặc đề xuất phù hợp tối ưu hoá hệ thống.</li>
            <li>Kỹ năng truyền đạt bằng lời và bằng văn bản tốt. Khả năng phỏng vấn, thuyết phục động viên.</li>
            <li>Có tư chất thu phục nhân tâm.</li>
            <li>Hoạt bát, nhanh nhẹn trong giao tiếp.</li>
            <li>Khả năng phân tích, phân loại, hệ thống hóa và đưa vào bảng các số liệu cần thiết liên quan đến việc quản trị nhân sự.</li>
        </ul>
    </li>
</ul>

<h3>2. Creative - Marketing & Sales</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Thấu hiểu nhu cầu của đối tượng khách hàng mục tiêu.</li>
            <li>Sáng tạo giải pháp và tư vấn giải pháp truyền thông sáng tạo cho Client dựa trên sự thấu hiểu đó.</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu Ứng viên</b>

        <ul>
            <li>Luôn luôn cởi mở, luôn luôn tươi mới, linh hoạt, thích ứng với nhiều đối tượng, nhiều môi trường, luôn học hỏi thêm nhiều điều mới.</li>
            <li>Năng khiếu nghệ thuật</li>
        </ul>
    </li>
</ul>

<p><b>Lí do phù hợp:</b></p>

<ul>
    <li>Khả năng giao tiếp, truyền đạt bằng lời nói và văn bản</li>
    <li>Với tính cách F - Feeling, họ quan tâm tới cảm xúc của mình và của người khác, kết hợp với I, họ hướng nội và thâm trầm.</li>
</ul>

<p>Tuy nhiên, trên đây chỉ là danh sách nghề nghiệp có thể sẽ thích hợp cho một INFP như bạn. Mục đích của nó là cho bạn một sự tham khảo chứ không phải là một bản danh sách chuẩn. Không có bất cứ một cam kết nào chứng tỏ rằng những sự nghiệp trên sẽ phù hợp hoàn toàn với bạn, nhưng cũng có thể, bạn sẽ tìm thấy nghề nghiệp phù hợp nhất cho bản thân trong danh sách này.</p>