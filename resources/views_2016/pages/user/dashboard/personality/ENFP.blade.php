<p>Chào bạn, nhóm tính cách của bạn là <b>ENFP</b>.</p>

<p>Một số đặc trưng của nhóm tính cách này:</p>

<ul>
    <li><b>ENFP</b> thích được người khác thừa nhận và hiểu họ.</li>
    <li><b>ENFP</b> có khả năng giao tiếp và viết lách rất tốt.</li>
    <li><b>ENFP</b> là nhà lãnh đạo bẩm sinh, nhưng không thích kiểm soát người khác.</li>
    <li><b>ENFP</b> có khả năng hiểu những mô hình, khái niệm lí thuyết khó.</li>
</ul>

<p>Với những đặc trưng chính như vậy, <b>ENFP</b> phù hợp với các ngành nghề:</p>

<h3>Nghiên cứu Thị trường - Marketing & Sales</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>
                Thu thập và phân tích dữ liệu định tính, định lượng về thị trường, về sản phẩm và người tiêu dùng;
            </li>

            <li>
                Tư vấn kết quả nghiên cứu cho Client và đề xuất giải pháp nhằm cải thiện hiệu quả marketing và bán hàng.
            </li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu Ứng viên</b>

        <ul>
            <li>Khả năng quan sát;</li>
            <li>Tư duy nghiên cứu định lượng sâu sắc;</li>
            <li>Khả năng phân tích để tìm ra ý nghĩa của từng dữ liệu, khả năng tổng hợp để xâu chuỗi vấn đề, nhìn ra bức tranh tổng thể;</li>
            <li>Ham tìm tòi, mở rộng hiểu biết trong nhiều lĩnh vực;</li>
            <li>Khả năng lĩnh hội nhanh chóng.</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>
<ul>
    <li>Những chuyên viên nghiên cứu thị trường luôn luôn cởi mở, hòa đồng, tươi mới và chủ động học hỏi tìm tòi. Chính vì thế, tính cách E - Extrovert rất phù hợp với họ.</li>
    <li>Trung tâm N - “Trực giác” của não bộ chịu trách nhiệm tìm hiểu, diễn dịch, và hình thành các mô hình từ thông tin thu thập được; sắp xếp các mô hình và liên hệ chúng với nhau. Nó giúp cho não bộ suy đoán các khả năng và tiên đoán tương lai. Và một trong những yêu cầu cần có của chuyên viên nghiên cứu thị trường là khả năng đưa ra các quyết định thực hiện dựa trên các dữ kiện thu thập được.</li>
    <li>Chuyên viên nghiên cứu thị trường đòi hỏi phải thấu hiểu nhu cầu của đối tượng khách hàng mục tiêu, nên họ phải có EQ cao (F - Feeling)</li>
    <li>Tính cách P - Perceiving rất cần thiết với một chuyên viên nghiên cứu thị trường bởi đặc điểm cởi mở khi thu nhận thông tin của họ. Nhà nghiên cứu thị trường cần có một cách nhìn nhận không phán xét, linh hoạt đưa ra những nhận định khi thị trường thay đổi.</li>
</ul>

<h3>2. Creative - Marketing & Sales</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>
                Thấu hiểu nhu cầu của đối tượng khách hàng mục tiêu.
            </li>
            <li>Sáng tạo giải pháp và tư vấn giải pháp truyền thông sáng tạo cho Client dựa trên sự thấu hiểu đó.</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu Ứng viên</b>

        <ul>
            <li>Luôn luôn cởi mở, luôn luôn tươi mới, linh hoạt, thích ứng với nhiều đối tượng, nhiều môi trường, luôn học hỏi thêm nhiều điều mới.</li>
            <li>Năng khiếu nghệ thuật</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>

<ul>
    <li>Công việc của Creative đòi hỏi họ phải giao lưu, tiếp xúc với nhiều người, thích ứng với nhiều đối tượng nên tính cách E - Extrovert rất phát triển trong họ.</li>
    <li>Nhờ có N - iNtuition mà những Creative luôn có thể tuôn trào những ý tưởng, cách nghĩ mới để ra được những sản phẩm độc đáo, thu hút mọi người.</li>
    <li>Creative đòi hỏi phải thấu hiểu nhu cầu của đối tượng khách hàng mục tiêu, nên họ phải có EQ cao, có F - Feeling để ra được sản phẩm đúng insight một cách tinh tế và tài tình.</li>
    <li>Những người đóng vai trò Creative trong công ty thường có tính cách P - Perceiving, họ tiếp cận thế giới một cách tự nhiên, tìm cách thích nghi với hoàn cảnh, thích một kết cục bỏ ngỏ, chấp nhận những cơ hội mới, và chấp nhận thay đổi kế hoạch.</li>
</ul>

<p>Tuy nhiên, trên đây chỉ là danh sách nghề nghiệp có thể sẽ thích hợp cho một <b>ENFP</b> như bạn. Mục đích của nó là cho bạn một sự tham khảo chứ không phải là một bản danh sách chuẩn. Không có bất cứ một cam kết nào chứng tỏ rằng những sự nghiệp trên sẽ phù hợp hoàn toàn với bạn, nhưng cũng có thể, bạn sẽ tìm thấy nghề nghiệp phù hợp nhất cho bản thân trong danh sách này.</p>


