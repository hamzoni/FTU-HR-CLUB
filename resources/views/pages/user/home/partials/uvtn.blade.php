    <script type="text/javascript">
        var uvtnGoPrev = function () {
            var currentIndex = $(".hrc-page-controls li.active").index();

            if (currentIndex != 0) {
                currentIndex --;

                $(".hrc-page-controls li").eq(currentIndex).find('a').trigger('click');
            }
        };

        var uvtnGoNext = function () {
            var currentIndex = $(".hrc-page-controls li.active").index();

            if (currentIndex != ($(".hrc-page-controls li").length - 1)) {
                currentIndex ++;

                $(".hrc-page-controls li").eq(currentIndex).find('a').trigger('click');
            }
        };

        var uvtnAlertWhenIdle;

        $(document).ready(function () {
            $(".hrc-page-controls a").on('click', function (e) {
                $(".hrc-page-controls li.active").removeClass('active');

                var parent = $(this).parent();
                parent.addClass('active');

                var source = $(this).data('source');
                var animate = 'fadeIn';

                $(".hrc-page-content").addClass('hide');
                $(source).removeClass('hide').animateCss(animate);

                if (parent.index() == ($(".hrc-page-controls li").length - 1)) {
                    uvtnAlertWhenIdle = window.setTimeout(function () {
                        alertWhenIdle.showModal();
                    }, 3000);
                } else {
                    if (uvtnAlertWhenIdle) {
                        window.clearTimeout(uvtnAlertWhenIdle);

                        uvtnAlertWhenIdle = null;
                    }
                }
            });

            $(".hrc-uvtn-prev").on('click', function (e) {
                e.preventDefault();

                uvtnGoPrev();
            });

            $(".hrc-uvtn-next").on('click', function (e) {
                e.preventDefault();

                uvtnGoNext();
            });

            $(".btn-go-to-personality-test-page").on('click', function (e) {
                e.preventDefault();

                $(".hrc-personality-explore-now").trigger('click');
            });
        });

        $(document).ready(function () {
            var timeOutId;

            $(window).bind('mousewheel', function(e) {
                if (timeOutId) {
                    window.clearTimeout(timeOutId);
                }

                timeOutId = window.setTimeout(function () {
                    if (e.originalEvent.wheelDelta / 120 > 0) {
                        uvtnGoPrev();
                    } else {
                        uvtnGoNext();
                    }
                }, 50);
            });

            var start,
                startX = '',               /* touchstart X coordinate */
                startY = '',               /* touchstart Y coordinate */
                end,
                endX = '',                 /* touchend X coordinate */
                endY = '',                 /* touchend Y coordinate */
                swipeDuration = 1000,      /* max touch duration */
                swipeDistanceX = 50,       /* X-axis min touch distance */
                swipeDistanceY = 50,       /* Y-axis min touch distance */
                thresholdX = 30,           /* X-axis max touch displacement */
                thresholdY = 30;           /* Y-axis max touch displacement */

            $(document).on("touchstart", "#hrc-uvtn", function (e, ui) {
                startX = e.originalEvent.touches[0].pageX;
                startY = e.originalEvent.touches[0].pageY;
                start = new Date().getTime(); /* touch start */
            }).on("touchend", "#hrc-uvtn", function (e, ui) {
                endX = e.originalEvent.changedTouches[0].pageX;
                endY = e.originalEvent.changedTouches[0].pageY;
                end = new Date().getTime(); /* touch end */

                if ((end - start) < swipeDuration) {
                    if (startX > endX && Math.abs(startY - endY) <= thresholdY && Math.abs(startX - endX) >= swipeDistanceX) {
                        uvtnGoNext();
                    } else if (startX < endX && Math.abs(startY - endY) <= thresholdY && Math.abs(startX - endX) >= swipeDistanceX) {
                        uvtnGoPrev();
                    }
                }
            });
        });
    </script>
@endpush

