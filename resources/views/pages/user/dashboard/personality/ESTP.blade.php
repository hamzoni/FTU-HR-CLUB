<p>Chào bạn, tính cách của bạn là <b>ESTP</b>.</p>

<p>Một số đặc trưng của nhóm tính cách này:</p>

<ul>
    <li><b>ESTP</b> có khả năng đặc biệt trong việc nắm bắt thái độ và động cơ của người khác</li>
    <li><b>ESTP</b> có năng lực đặc biệt trong việc tạo năng lượng tích cực</li>
    <li><b>ESTP</b> là những người thực tế, có óc quan sát, thích đùa, thích mạo hiểm với khả năng phản ứng nhanh xuất sắc và tìm ra giải pháp cho vấn đề</li>
    <li><b>ESTP</b> thường gặp vấn đề trong học tập, đặc biệt là các chương trình học cao hơn có liên quan nhiều đến lý thuyết</li>
</ul>

<p>Với những đặc trưng như vậy, bạn có thể phù hợp với:</p>

<h3>1. Bán hàng - Marketing & Sales</h3>

<ul>
    <li>
        <b>Mô tả công việc:</b>

        <ul>
            <li>Lên kế hoạch bán hàng, đảm bảo doanh số, lợi nhuận cho nhãn hàng. Trực tiếp tiếp cận khách hàng, tư vấn giúp khách hàng chọn lựa mặt hàng - dịch vụ phù hợp, thuyết phục khách hàng mua mặt hàng đã được tư vấn</li>
        </ul>

    </li>

    <li>
        <b>Yêu cầu Ứng viên</b>

        <ul>
            <li>Giao tiếp, đàm phán, thuyết phục tốt</li>
            <li>Óc quan sát, lắng nghe, thấu hiểu hành vi người mua hàng</li>
            <li>Linh hoạt, thích ứng với nhiều hoàn cảnh, nhiều đối tượng và thị trường khác nhau</li>
            <li>Bền bỉ, chịu đựng áp lực doanh số, không nhanh nản, lăn xả, năng động</li>
            <li>Tính cạnh tranh cao</li>
        </ul>
    </li>
</ul>

<h3>2. Tuyển dụng nhân sự - Human Resources</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Thiết lập chiến dịch tuyển dụng nhân sự</li>
            <li>Phân tích nhu cầu sử dụng lao động của doanh nghiệp</li>
            <li>Xác định những vấn đề tuyển dụng cụ thể: doanh nghiệp cần có những lao động như thế nào? Thời điểm nào nên tuyển dụng? Những lao động hiện tại của doanh nghiệp đã đáp ứng được yêu cầu công việc chưa?</li>
            <li>Giám sát, tư vấn quá trình tuyển dụng.</li>
            <li>Hoạch định chính sách về chế độ của công ty để thu hút nhân lực.</li>
            <li>Điều phối phòng nhân sự tham gia tuyển dụng các vị trí còn thiếu cho các phòng ban.</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu ứng viên</b>

        <ul>
            <li>Tư duy nhìn nhận vấn đề hệ thống, logic.</li>
            <li>Có tầm nhìn chiến lược về định hướng phát triển của công ty. Có khả năng nhạy bén về các thay đổi trong cơ cấu công ty để có các kế hoạch hoặc đề xuất phù hợp tối ưu hoá hệ thống.</li>
            <li>Kỹ năng truyền đạt bằng lời và bằng văn bản tốt. Khả năng phỏng vấn, thuyết phục động viên.</li>
            <li>Có tư chất thu phục nhân tâm.</li>
            <li>Hoạt bát, nhanh nhẹn trong giao tiếp.</li>
            <li>Khả năng phân tích, phân loại, hệ thống hóa và đưa vào bảng các số liệu cần thiết liên quan đến việc quản trị nhân sự.</li>
        </ul>
    </li>
</ul>

<h3>3. Quản lý quan hệ khách hàng - Logistics</h3>

<ul>
    <li>
        <b>Mô tả công việc:</b>

        <ul>
            <li>
                Lên kế hoạch và chỉ đạo hoạt động của đội ngũ chăm sóc khách hàng để đáp ứng mọi nhu cầu khách hàng. Phát triển quy trình, thiết lập các tiêu chuẩn, giám sát hoạt động quản lý đảm bảo đơn hàng chính xác, hiệu quả và nhanh chóng đến tay khách hàng. Chịu trách nhiệm đáp ứng các yêu cầu và giải quyết các vấn đề của khách hàng
            </li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu Ứng viên</b>

        <ul>
            <li>Khả năng giao tiếp khéo léo để làm việc với khách hàng</li>
            <li>Sự nhanh nhạy, linh hoạt trong công việc</li>
            <li>Năng động, sáng tạo, nhiệt huyết, có tinh thần trách nhiệm cao</li>
            <li>Khả năng bao quát, giải quyết tình huống</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>

<ul>
    <li>Kỹ năng giao tiếp tuyệt vời: Các <b>ESTP</b> không thực sự tìm cách dẫn dắt hay quản lý con người, nhưng họ có xu hướng có các kỹ năng kết nối mọi người tuyệt vời và bản năng biết làm thế nào để tận dụng tốt nhất tất cả các tương tác xã hội. Và họ</li>
    <li>Họ cũng có khả năng phản ứng nhanh và hiệu quả cho những việc cấp thiết như cấp cứu hay trong một hoàn cảnh hỗn loạn.</li>
    <li>Các <b>ESTP</b> thích chấp nhận các rủi ro, lớn và nhỏ, và thường sẽ là người thắng vì vậy, họ thích lựa chọn con đường kém ổn định nhưng thú vị hơn là sự nghiệp an toàn nhưng nhàm chán</li>
    <li><b>ESTP</b> cũng thích những trải nghiệm tiếp xúc với con người, không bị ràng buộc, sai khiến</li>
    <li>Khả năng quan sát của họ khiến cho họ trở nên cực kỳ giỏi trong việc phân tích và đánh giá thái độ và nhận định của người khác một cách chính xác. Khả năng đối nhân xử thế của họ cho phép họ có lợi thế trong việc tiếp xúc với con người. Vì lí do này, <b>ESTP</b> là người bán hàng rất giỏi</li>
</ul>

