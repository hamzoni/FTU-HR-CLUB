<p>Chào bạn, tính cách của bạn là <b>ESFP</b>.</p>

<p>Một số đặc trưng của người mang tính cách <b>ESFP</b>.</p>

<ul>
    <li><b>ESFP</b> có kĩ năng giao tiếp rất tốt, và có thể hay bắt gặp chính họ trong vai trò của người giảng hòa</li>
    <li>Những người <b>ESFP</b> thường rất thực tế dù là họ ghét khuôn mẫu và những việc lặp đi lặp lại</li>
    <li>Các <b>ESFP</b> rất tinh ý, có thể nhận thấy và đáp ứng trạng thái cảm xúc đau khổ của người khác</li>
    <li><b>ESFP</b> thường luôn có cảm giác liên kết mạnh mẽ</li>
    <li><b>ESFP</b> ham muốn sự phấn khích, kích thích và mới lạ</li>
    <li><b>ESFP</b> được đánh giá cao về khiếu thẩm mĩ, và nhận thức tốt về hình học không gian và hàm số.</li>
</ul>

<p>Với những đặc trưng như vậy, bạn có thể phù hợp với:</p>

<h3>1. Bán hàng - Marketing & Sales</h3>

<ul>
    <li>
        <b>Mô tả công việc:</b>

        <ul>
            <li>Lên kế hoạch bán hàng, đảm bảo doanh số, lợi nhuận cho nhãn hàng. Trực tiếp tiếp cận khách hàng, tư vấn giúp khách hàng chọn lựa mặt hàng - dịch vụ phù hợp, thuyết phục khách hàng mua mặt hàng đã được tư vấn</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu Ứng viên</b>

        <ul>
            <li>Giao tiếp, đàm phán, thuyết phục tốt</li>
            <li>Óc quan sát, lắng nghe, thấu hiểu hành vi người mua hàng</li>
            <li>Linh hoạt, thích ứng với nhiều hoàn cảnh, nhiều đối tượng và thị trường khác nhau</li>
            <li>Bền bỉ, chịu đựng áp lực doanh số, không nhanh nản, lăn xả, năng động</li>
            <li>Tính cạnh tranh cao</li>
        </ul>
    </li>
</ul>

<h3>2. Quản lý quan hệ khách hàng - Logistics</h3>

<ul>
    <li>
        <b>Mô tả công việc:</b>

        <ul>
            <li>
                Lên kế hoạch và chỉ đạo hoạt động của đội ngũ chăm sóc khách hàng để đáp ứng mọi nhu cầu khách hàng. Phát triển quy trình, thiết lập các tiêu chuẩn, giám sát hoạt động quản lý đảm bảo đơn hàng chính xác, hiệu quả và nhanh chóng đến tay khách hàng. Chịu trách nhiệm đáp ứng các yêu cầu và giải quyết các vấn đề của khách hàng
            </li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu Ứng viên</b>

        <ul>
            <li>Khả năng giao tiếp khéo léo để làm việc với khách hàng</li>
            <li>Sự nhanh nhạy, linh hoạt trong công việc</li>
            <li>Năng động, sáng tạo, nhiệt huyết, có tinh thần trách nhiệm cao</li>
            <li>Khả năng bao quát, giải quyết tình huống</li>
        </ul>
    </li>
</ul>

<h3>3. Creative - Marketing & Sales</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Thấu hiểu nhu cầu của đối tượng khách hàng mục tiêu</li>
            <li>Sáng tạo giải pháp và tư vấn giải pháp truyền thông sáng tạo cho client dựa trên sự thấu hiểu đó</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu Ứng viên</b>

        <ul>
            <li>Sáng tạo, tuôn trào ý tưởng, cách nghĩ mới</li>
            <li>Năng khiếu nghệ thuật</li>
            <li>Chịu được áp lực cao, đam mê nghề</li>
            <li>Luôn luôn cởi mở, luôn luôn tươi mới, linh hoạt, thích ứng với nhiều đối tượng, nhiều môi trường, luôn học hỏi thêm nhiều điều mới & học rất nhanh</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>

<ul>
    <li>Là những người ưa thích những điều mới mẻ, cộng thêm nét tính các P - hành động thường không thích lên kế hoạch, các <b>ESFP</b> thực sự bị kích thích bởi việc gặp gỡ những con người mới và trải nghiệm mới.</li>
    <li>Tính thực tế: Tính cách của các <b>ESFP</b> là sống trong giây phút hiện tại và tập trung hoàn toàn vào những gì đang xảy ra "ở đây và bây giờ". Họ rất dễ dàng để nhận thấy sự thật, những thứ hữu hình và sự thay đổi.</li>
    <li><b>ESFP</b> thích giao tiếp lại thêm tính cách hướng ngoại thường gây thiện cảm với những người xung quanh vậy nên giao tiếp không phải là trở ngại với họ</li>
    <li>Với cảm giác thẩm mỹ phát triển cao, cộng thêm sự sáng tạo đa dạng đã tạo nên một trong những đặc điểm tính cách mạnh nhất của <b>ESFP</b></li>
    <li>Do tính cách sáng tạo, luôn tìm kiếm sự tươi mới, các <b>ESFP</b> biết cách khiến công việc không nhàm chán, từ đó, những áp lực trong công việc cũng giảm đi khiến cho môi trường làm việc của họ trở nên lý tưởng hơn</li>
</ul>

<p>Tuy nhiên, trên đây chỉ là danh sách nghề nghiệp có thể sẽ thích hợp cho một <b>ESFP</b> như bạn. Mục đích của nó là cho bạn một sự tham khảo chứ không phải là một bản danh sách chuẩn. Không có bất cứ một cam kết nào chứng tỏ rằng những sự nghiệp trên sẽ phù hợp hoàn toàn với bạn, nhưng cũng có thể, bạn sẽ tìm thấy nghề nghiệp phù hợp nhất cho bản thân trong danh sách này.</p>

