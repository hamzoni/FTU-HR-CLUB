<p>Chào bạn, tính cách của bạn là <b>ESTJ</b>.</p>

<p>Một số đặc trưng của người mang tính cách <b>ESTJ</b>:</p>

<ul>
    <li>3 đặc trưng trong tính cách các <b>ESTJ</b>: nguyên tắc, truyền thống, ổn định</li>
    <li><b>ESTJ</b> là người có trách nhiệm cao và luôn độc lập trong mọi việc</li>
    <li><b>ESTJ</b> thường có ý chí mạnh mẽ và không sợ lên tiếng và bảo vệ ý kiến ​​của mình</li>
    <li><b>ESTJ</b> đánh giá cao sự an ninh và trật tự xã hội</li>
    <li><b>ESTJ</b> luôn nỗ lực hết mình trong hầu hết những việc mà họ làm</li>
    <li><b>ESTJ</b> cần phải chú ý đến xu hướng trở nên quá cứng nhắc và cứ chăm chăm vào những tiểu tiết của mình.</li>
</ul>

<p>Với những đặc trưng như vậy, bạn có thể phù hợp với:</p>

<h3>1. Vận hành Tài chính - Finance</h3>

<ul>
    <li>
        <b>Mô tả công việc:</b>

        <ul>
            <li>Dựa trên Kế hoạch tài chính và Kiểm sát tài chính để vận hành bộ máy Tài chính trong Doanh nghiệp, chịu trách nhiệm ở tất cả quyết định Tài chính như: Quyết định đầu tư, phân phối vốn, lên kế hoạch ngân sách, phân chia lợi nhuận...</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu ứng viên</b>

        <ul>
            <li>Kỹ năng thiết lập mục tiêu</li>
            <li>Kỹ năng xây dựng kế hoạch thực thi, tổ chức và sắp xếp công việc</li>
            <li>Kỹ năng quản lý, làm việc với con người, bao gồm nhiều kỹ năng khác như: Kỹ năng lắng nghe, thấu hiểu con người; giao tiếp; xây dựng nhóm, phát triển thành viên; ra quyết định;...</li>
        </ul>
    </li>
</ul>

<h3>2. Quản lý Nguyên vật liệu - Logistics</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Chịu trách nhiệm toàn bộ nguyên vật liệu đầu vào quá trình sản xuất. Phối hợp với bộ phận mua hàng, sản xuất và cung ứng để đảm bảo hiệu quả sử dụng nguyên vật liệu.</li>
            <li>Quản lý quá trình giao nhận, lưu kho, lưu bãi, làm thủ tục hải quan đối với nguyên vật liệu đầu vào cho Doanh nghiệp.</li>
            <li>Giám sát dòng di chuyển và phân phối nguyên vật liệu nhằm nâng cao hiệu quả, tốc độ và chất lượng vận tải.</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu ứng viên</b>

        <ul>
            <li>Kỹ năng sắp xếp, quản lý công việc</li>
            <li>Khả năng đưa ra quyết định nhanh trong những trường hợp cấp bách</li>
            <li>Cẩn thận, tỉ mỉ, trung thực khi làm việc với nhiều sổ sách hành chính quan trọng</li>
            <li>Trách nhiệm, chỉn chu trong công việc.</li>
        </ul>
    </li>
</ul>


<h3>3. Quản lý Vận tải - Logistics</h3>

<ul>
