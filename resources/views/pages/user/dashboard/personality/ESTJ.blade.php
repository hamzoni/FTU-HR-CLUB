        <b>Mô tả công việc:</b>

        <ul>
            <li>Chỉ đạo hiệu quả hệ thống vận tải của Doanh nghiệp. Chịu trách nhiệm lập kế hoạch, định tuyến, quản lý ngân sách, đàm phán hợp đồng đảm bảo hàng hóa, dịch vụ đến tay khách hàng đúng địa điểm, số lượng và chất lượng</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu ứng viên</b>

        <ul>
            <li>Kỹ năng sắp xếp, quản lý công việc</li>
            <li>Quyết đoán trong công việc</li>
            <li>Cẩn thận, tỉ mỉ, trung thực</li>
            <li>Trách nhiệm, chỉn chu với công việc</li>
            <li>Kĩ năng giao tiếp, làm việc nhóm,...</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>

<ul>
    <li><b>ESTJ</b> thích hợp nhất cho những công việc đòi hỏi phải thiết lập trật tự và cấu trúc.</li>
    <li>Là người trách nhiệm, luôn dồn tâm trí, sức lực để làm mọi thứ thật tốt nên họ giỏi trong nhiều việc khác nhau.</li>
    <li>Mang đặc điểm S, họ trung thực và thẳng thắn. Họ có một tầm nhìn rõ ràng và sự hiểu biết về những gì là chấp nhận được và những gì không thể được - điều này thường làm cho <b>ESTJ</b> trở thành lãnh đạo hay cán bộ quản lý xuất sắc, mặc dù cấp dưới đôi khi có thể phàn nàn về tính cứng nhắc của họ.</li>
    <li>T - Thinking khiến những quyết định đưa ra của <b>ESTJ</b> luôn khách quan, dựa trên lập luận logic và sự thật, không dễ bị lung lay bởi người khác.</li>
    <li>E - Extraversion: mang tính cách hướng ngoại, việc giao tiếp, tương tác với tập thể và mọi người xung quanh là một lợi thế của <b>ESTJ</b>. Nhược điểm đưa ra quyết định mà không suy nghĩ nhiều của tính cách E khi đi cùng với T sẽ được hài hòa, giảm bớt.</li>
    <li>Tính cách J phát triển rất phù hợp với tính chất công việc này: họ minh bạch, thông thái, trách nhiệm và có phần nghiêm khắc.</li>
</ul>

