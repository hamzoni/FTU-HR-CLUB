<p>Chào bạn, nhóm tính cách của bạn là <b>ENTP</b>.</p>

<p>Một số đặc trưng của nhóm tính cách này:</p>

<ul>
    <li><b>ENTP</b> thích đưa ra ý tưởng và giả thuyết.</li>
    <li><b>ENTP</b> linh hoạt và đa dạng trong cách tiếp nhận thông tin, họ thường không phán xét và nhìn nhận vấn đề từ nhiều hướng.</li>
    <li><b>ENTP</b> không thích những kế hoạch làm việc cũng như môi trường làm việc bị hạn chế.</li>
</ul>

<p>Với những đặc trưng chính như vậy, <b>ENTP</b> phù hợp với các ngành nghề:</p>

<h3>1. Nghiên cứu Thị trường - Marketing & Sales</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Thu thập và phân tích dữ liệu định tính, định lượng về thị trường, về sản phẩm và người tiêu dùng</li>
            <li>Tư vấn kết quả nghiên cứu cho client và đề xuất giải pháp nhằm cải thiện hiệu quả marketing và bán hàng</li>
            Yêu cầu ứng viên
            <li>Khả năng giao tiếp xuất sắc để thuyết phục client với các kết quả nghiên cứu</li>
            <li>Tư duy định lượng, fact & data, no assumption</li>
            <li>Khả năng phân tích để tìm ra ý nghĩa của từng dữ liệu, khả năng tổng hợp để xâu chuỗi vấn đề, nhìn ra bức tranh lớn và cốt lõi</li>
            <li>Lắng nghe & quan sát, thấu hiểu và thông cảm với con người</li>
            <li>Tò mò, cởi mở với những ý tưởng mới</li>
            <li>Luôn luôn cởi mở, luôn luôn tươi mới, linh hoạt, thích ứng với nhiều đối tượng, nhiều môi trường, luôn học hỏi thêm nhiều điều mới & học rất nhanh</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>

<ul>
    <li>Những chuyên viên nghiên cứu thị trường luôn luôn cởi mở, hòa đồng và chủ động học hỏi tìm tòi những điều mới mẻ. Chính vì thế, tính cách E - Extrovert rất cần ở những con người này.</li>
    <li>Trung tâm N - “Trực giác” của não bộ chịu trách nhiệm tìm hiểu, diễn dịch, và hình thành các mô hình từ thông tin thu thập được; sắp xếp các mô hình và liên hệ chúng với nhau. Nó giúp cho não bộ suy đoán các khả năng và tiên đoán tương lai. Và một trong những yêu cầu cần có của chuyên viên nghiên cứu thị trường là khả năng đưa ra các quyết định thực hiện dựa trên các dữ kiện thu thập được.</li>
    <li>Những chuyên viên nghiên cứu thị trường cần có tính cách T - Thinking để có khả năng phân tích những dữ liệu định tính, định lượng một cách chính xác và khách quan.</li>
    <li>Tính cách P - Perceiving rất cần thiết với một chuyên viên nghiên cứu thị trường bởi đặc điểm cởi mở khi thu nhận thông tin của họ. Nhà nghiên cứu thị trường cần có một cách nhìn nhận không phán xét, linh hoạt đưa ra những nhận định khi thị trường thay đổi.</li>
</ul>

<h3>2. Chuyên viên Kinh doanh - Marketing & Sales</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Lên kế hoạch bán hàng, đảm bảo doanh số, lợi nhuận cho nhãn hàng</li>
            <li>Trực tiếp tiếp cận khách hàng, tư vấn giúp khách hàng chọn lựa mặt hàng - dịch vụ phù hợp, thuyết phục khách hàng mua mặt hàng đã được tư vấn</li>
            Yêu cầu ứng viên
            <li>Giao tiếp, đàm phán, thuyết phục xuất sắc</li>
            <li>Óc quan sát, lắng nghe, thấu hiểu hành vi người mua hàng</li>
            <li>Linh hoạt, thích ứng với nhiều hoàn cảnh, nhiều đối tượng và thị trường khác nhau</li>
            <li>Bền bỉ, chịu đựng áp lực doanh số, không nhanh nản, lăn xả, năng động</li>
            <li>Tính cạnh tranh cao</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>
<ul>
    <li>Chuyên viên Kinh doanh cần có tính cách E - Extrovert để có khả năng giao tiếp lưu loát, đầu óc nhanh nhạy, đàm phán, thuyết phục khách hàng một cách xuất sắc, khéo léo.</li>
    <li>Chuyên viên Kinh doanh là một nghề đòi hỏi sự nhanh nhẹn, linh hoạt trong công việc, nên họ cần có N - “Trực giác” nhạy bén để giao tiếp được với mọi đối tượng khách hàng, thấu hiểu hành vi người mua hàng.</li>
    <li>ENTP thích thu thập thông tin hơn ra quyết định, họ khá lí trí (T - Thinking) và logic trong việc đi đến kết luận. Khi áp dụng khả năng tư duy vào trực giác của mình, kết quả có thể cực kỳ ấn tượng. Một ENTP điển hình có tầm nhìn rất xa, đầy khả năng sáng tạo và luôn dám nghĩ dám làm.</li>
    <li>Tính cách P - Perceiving không thể thiếu ở một chuyên viên Kinh doanh, vì họ cần khả năng linh hoạt, thích ứng với nhiều hoàn cảnh, nhiều đối tượng và thị trường khác nhau.</li>
</ul>

<h3>3. Chuyên viên Đào tạo, phát triển - Human Resources</h3>

<ul>
    <li>
        <b>Mô tả công việc</b>

        <ul>
            <li>Rà soát và đánh giá các yêu cầu về phát triển kiến thức, kỹ năng nghề nghiệp của nhân viên, đảm bảo đáp ứng cho yêu cầu hiện tại và tương lai của doanh nghiệp.</li>
            <li>Hoạch định và thực hiện các kế hoạch đào tạo và phát triển nhân lực:</li>
            <li>Xác định chiến lược phát triển của doanh nghiệp, đào tạo năng lực nhân viên để nâng cao chất lượng nhân sự phù hợp với định hướng chiến lược.</li>
            <li>Tổ chức nhiều chương trình hội thảo, đào tạo inhouse, tham gia khóa học nhân sự hoặc cử cán bộ chuyên ngành đi học tập tại các trung tâm đào tạo năng lực cán bộ.</li>
            <li>Xây dựng nhiều buổi giao lưu, chia sẻ kinh nghiệm giữa các nhân viên, hoặc từ các cấp quản lý đến nhân viên.</li>
            <li>Quan tâm đến nhân sự đang làm việc tại công ty, cố gắng giúp họ phát huy khả năng. Đồng thời, tạo điều kiện cho những nhân sự mới hòa nhập tốt hơn với văn hóa làm việc.</li>
            <li>Xây dựng các chương trình tuyển dụng chiến lược. Ví dụ: MT, Intern, Tài trợ, Hợp tác.</li>
        </ul>
    </li>

    <li>
        <b>Yêu cầu ứng viên</b>

        <ul>
            <li>Kỹ năng quan hệ cá nhân tốt</li>
            <li>Kỹ năng làm việc dưới áp lực cao</li>
            <li>Kỹ năng làm việc nhóm</li>
            <li>Tính cách khéo léo, kiên trì và nhạy cảm</li>
        </ul>
    </li>
</ul>

<p><b>Lý do phù hợp</b></p>

<ul>
    <li>Những chuyên viên đào tạo, phát triển nhân sự là những người vui vẻ và đầy nhiệt huyết, có khả năng thúc đẩy người khác, họ khéo léo trong cách đối nhân xử thế, vì vậy, tính cách E - Extrovert rất phù hợp với họ.</li>
    <li>Trung tâm N - “Trực giác” của não bộ chịu trách nhiệm tìm hiểu, diễn dịch, và hình thành các mô hình từ thông tin thu thập được; sắp xếp các mô hình và liên hệ chúng với nhau. Nó giúp cho não bộ suy đoán các khả năng và tiên đoán tương lai. Và một trong những yêu cầu cần có của người đào tạo, phát triển nhân sự là khả năng đưa ra các quyết định thực hiện dựa trên các dữ kiện thu thập được.</li>
    <li>Chuyên viên đào tạo, phát triển cần có tư duy lý trí (T - Thinking) để hiểu được vấn đề một cách nhanh chóng, chính xác và phản ứng một cách khách quan, lí trí.</li>
    <li>Chuyên viên đào tạo, phát triển nhân sự thường có tính cách P  - Perceiving để có thể linh hoạt trong cách đối nhân xử thế, linh hoạt trong việc giải quyết các tình huống.</li>
</ul>

<p>Tuy nhiên, trên đây chỉ là danh sách nghề nghiệp có thể sẽ thích hợp cho một <b>ENTP</b> như bạn. Mục đích của nó là cho bạn một sự tham khảo chứ không phải là một bản danh sách chuẩn. Không có bất cứ một cam kết nào chứng tỏ rằng những sự nghiệp trên sẽ phù hợp hoàn toàn với bạn, nhưng cũng có thể, bạn sẽ tìm thấy nghề nghiệp phù hợp nhất cho bản thân trong danh sách này.</p>


