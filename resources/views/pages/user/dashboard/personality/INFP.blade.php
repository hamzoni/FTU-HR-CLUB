<p>Chào bạn, tính cách của bạn là <b>INFP</b>.</p>

<p>Một số đặc trưng của người mang tính cách ISFJ:</p>

<ul>
    <li>Các <b>INFP</b> tìm kiếm sự hài hòa trong cuộc sống của họ và môi trường xung quanh, thường cảm thấy thất vọng vì tất cả những điều xấu xảy ra trên thế giới và cố gắng để tạo ra một cái gì đó tích cực.</li>
    <li>Họ cũng có khả năng tuyệt vời trong việc sáng tạo ra các biểu tượng hay giải thích cách biểu tượng.</li>
    <li>Không giống như các loại NT - theo quan điểm của họ, logic không phải là luôn luôn cần thiết. Các <b>INFP</b> sẽ thích thú thảo luận với giả thuyết hay triết học nhiều hơn bất kỳ loại nào khác.</li>
    <li>Các <b>INFP</b> có đặc điểm chung với các loại NF - họ năng khiếu về ngoại ngữ là rất đặc biệt.</li>
    <li>cực kỳ sáng tạo, đổi mới và định hướng được mục tiêu - họ có thể là những người ủng hộ tuyệt vời cho những nguyên nhân họ thực sự tin tưởng.</li>
    <li>ÌNFP năng khiếu về ngôn ngữ là rất đặc biệt.</li>
    <li><b>INFP</b> có thể dễ dàng phản ánh và truyền tải ý tưởng của mình.</li>
</ul>

